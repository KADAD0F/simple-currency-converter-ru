def validate_api_response(data, expected_base=None):
    """–ü—Ä–æ–≤–µ—Ä—è–µ—Ç —Ü–µ–ª–æ—Å—Ç–Ω–æ—Å—Ç—å –∏ –∫–æ—Ä—Ä–µ–∫—Ç–Ω–æ—Å—Ç—å –¥–∞–Ω–Ω—ã—Ö, –ø–æ–ª—É—á–µ–Ω–Ω—ã—Ö –æ—Ç API
    
    Args:
        data (dict): –î–∞–Ω–Ω—ã–µ, –ø–æ–ª—É—á–µ–Ω–Ω—ã–µ –æ—Ç API
        expected_base (str, optional): –û–∂–∏–¥–∞–µ–º–∞—è –±–∞–∑–æ–≤–∞—è –≤–∞–ª—é—Ç–∞
    
    Returns:
        tuple: (bool, str) - —Ñ–ª–∞–≥ –≤–∞–ª–∏–¥–Ω–æ—Å—Ç–∏ –∏ —Å–æ–æ–±—â–µ–Ω–∏–µ
    """
    required_fields = ['rates', 'base', 'date']
    for field in required_fields:
        if field not in data:
            return False, f"–û—Ç–≤–µ—Ç API –Ω–µ —Å–æ–¥–µ—Ä–∂–∏—Ç –æ–±—è–∑–∞—Ç–µ–ª—å–Ω–æ–µ –ø–æ–ª–µ: {field}"
    
    # –ü—Ä–æ–≤–µ—Ä—è–µ–º, —á—Ç–æ rates - —ç—Ç–æ —Å–ª–æ–≤–∞—Ä—å
    if not isinstance(data['rates'], dict):
        return False, "–ü–æ–ª–µ 'rates' –¥–æ–ª–∂–Ω–æ –±—ã—Ç—å —Å–ª–æ–≤–∞—Ä–µ–º"
    
    # –ü—Ä–æ–≤–µ—Ä—è–µ–º, —á—Ç–æ –±–∞–∑–æ–≤–∞—è –≤–∞–ª—é—Ç–∞ —Å–æ–æ—Ç–≤–µ—Ç—Å—Ç–≤—É–µ—Ç –æ–∂–∏–¥–∞–µ–º–æ–π
    if expected_base and data['base'] != expected_base:
        return False, f"–ë–∞–∑–æ–≤–∞—è –≤–∞–ª—é—Ç–∞ {data['base']} –Ω–µ —Å–æ–æ—Ç–≤–µ—Ç—Å—Ç–≤—É–µ—Ç –æ–∂–∏–¥–∞–µ–º–æ–π {expected_base}"
    
    return True, "–î–∞–Ω–Ω—ã–µ –≤–∞–ª–∏–¥–Ω—ã"

def fetch_rates():
    """–ó–∞–≥—Ä—É–∂–∞–µ—Ç –∞–∫—Ç—É–∞–ª—å–Ω—ã–µ –∫—É—Ä—Å—ã –≤–∞–ª—é—Ç –∏–∑ API —Å –æ–±—Ä–∞–±–æ—Ç–∫–æ–π –≤–æ–∑–º–æ–∂–Ω—ã—Ö –æ—à–∏–±–æ–∫
    
    Returns:
        dict or None: –î–∞–Ω–Ω—ã–µ —Å –∫—É—Ä—Å–∞–º–∏ –≤–∞–ª—é—Ç –∏–ª–∏ None –ø—Ä–∏ –Ω–µ—É–¥–∞—á–µ
    """
    for api_url in API_URLS:
        try:
            # –û–ø—Ä–µ–¥–µ–ª—è–µ–º –æ–∂–∏–¥–∞–µ–º—É—é –±–∞–∑–æ–≤—É—é –≤–∞–ª—é—Ç—É –∏–∑ URL
            expected_base = api_url.split('/')[-1]
            
            print(f"–ü–æ–ø—ã—Ç–∫–∞ –∑–∞–≥—Ä—É–∑–∫–∏ –¥–∞–Ω–Ω—ã—Ö —Å {api_url}")
            response = requests.get(api_url, timeout=10)
            response.raise_for_status()
            data = response.json()
            
            # –ü—Ä–æ–≤–µ—Ä–∫–∞ —Ü–µ–ª–æ—Å—Ç–Ω–æ—Å—Ç–∏ –¥–∞–Ω–Ω—ã—Ö
            is_valid, message = validate_api_response(data, expected_base)
            if not is_valid:
                print(f"‚ö†Ô∏è –ü—Ä–µ–¥—É–ø—Ä–µ–∂–¥–µ–Ω–∏–µ: {message} –æ—Ç {api_url}")
                continue
                
            # –î–æ–±–∞–≤–ª—è–µ–º –¥–∞—Ç—É –ø–æ–ª—É—á–µ–Ω–∏—è –¥–∞–Ω–Ω—ã—Ö
            try:
                locale.setlocale(locale.LC_TIME, '')
                date_str = datetime.now().strftime("%x")
            except:
                date_str = datetime.now().strftime("%d.%m.%Y")
                
            data['date_fetched'] = date_str
            data['timestamp'] = int(time.time())
            return data
        except requests.exceptions.RequestException as e:
            print(f"‚ö†Ô∏è –û—à–∏–±–∫–∞ –∑–∞–ø—Ä–æ—Å–∞ –∫ {api_url}: {str(e)}")
        except json.JSONDecodeError:
            print(f"‚ö†Ô∏è –û—à–∏–±–∫–∞ –¥–µ–∫–æ–¥–∏—Ä–æ–≤–∞–Ω–∏—è JSON –æ—Ç {api_url}")
        except Exception as e:
            print(f"‚ö†Ô∏è –ù–µ–∏–∑–≤–µ—Å—Ç–Ω–∞—è –æ—à–∏–±–∫–∞ –ø—Ä–∏ –∑–∞–ø—Ä–æ—Å–µ –∫ {api_url}: {str(e)}")
    
    return None

