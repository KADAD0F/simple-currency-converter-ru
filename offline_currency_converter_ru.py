def perform_conversion(rates_data, src_code, tgt_code, amount):
    """–í—ã–ø–æ–ª–Ω—è–µ—Ç –∫–æ–Ω–≤–µ—Ä—Ç–∞—Ü–∏—é –≤–∞–ª—é—Ç —Å –ø—Ä–æ–≤–µ—Ä–∫–æ–π –∫–æ—Ä—Ä–µ–∫—Ç–Ω–æ—Å—Ç–∏ –¥–∞–Ω–Ω—ã—Ö
    
    Args:
        rates_data (dict): –î–∞–Ω–Ω—ã–µ —Å –∫—É—Ä—Å–∞–º–∏ –≤–∞–ª—é—Ç
        src_code (str): –ö–æ–¥ –∏—Å—Ö–æ–¥–Ω–æ–π –≤–∞–ª—é—Ç—ã
        tgt_code (str): –ö–æ–¥ —Ü–µ–ª–µ–≤–æ–π –≤–∞–ª—é—Ç—ã
        amount (float): –°—É–º–º–∞ –¥–ª—è –∫–æ–Ω–≤–µ—Ä—Ç–∞—Ü–∏–∏
    
    Returns:
        float: –†–µ–∑—É–ª—å—Ç–∞—Ç –∫–æ–Ω–≤–µ—Ä—Ç–∞—Ü–∏–∏
    
    Raises:
        ValueError: –ü—Ä–∏ –æ—à–∏–±–∫–∞—Ö –≤ –¥–∞–Ω–Ω—ã—Ö
    """
    # –ü—Ä–æ–≤–µ—Ä—è–µ–º –Ω–∞–ª–∏—á–∏–µ –≤–∞–ª—é—Ç –≤ –¥–∞–Ω–Ω—ã—Ö
    if src_code not in rates_data['rates']:
        raise ValueError(f"–ò—Å—Ö–æ–¥–Ω–∞—è –≤–∞–ª—é—Ç–∞ {src_code} –æ—Ç—Å—É—Ç—Å—Ç–≤—É–µ—Ç –≤ –¥–∞–Ω–Ω—ã—Ö")
    if tgt_code not in rates_data['rates']:
        raise ValueError(f"–¶–µ–ª–µ–≤–∞—è –≤–∞–ª—é—Ç–∞ {tgt_code} –æ—Ç—Å—É—Ç—Å—Ç–≤—É–µ—Ç –≤ –¥–∞–Ω–Ω—ã—Ö")
    
    src_rate = rates_data['rates'][src_code]
    tgt_rate = rates_data['rates'][tgt_code]
    
    # –ü—Ä–æ–≤–µ—Ä—è–µ–º, —á—Ç–æ –∫—É—Ä—Å—ã –Ω–µ —Ä–∞–≤–Ω—ã –Ω—É–ª—é
    if src_rate <= 0:
        raise ValueError(f"–ö—É—Ä—Å –∏—Å—Ö–æ–¥–Ω–æ–π –≤–∞–ª—é—Ç—ã {src_code} —Ä–∞–≤–µ–Ω {src_rate}, —á—Ç–æ –Ω–µ–¥–æ–ø—É—Å—Ç–∏–º–æ")
    if tgt_rate <= 0:
        raise ValueError(f"–ö—É—Ä—Å —Ü–µ–ª–µ–≤–æ–π –≤–∞–ª—é—Ç—ã {tgt_code} —Ä–∞–≤–µ–Ω {tgt_rate}, —á—Ç–æ –Ω–µ–¥–æ–ø—É—Å—Ç–∏–º–æ")
    
    # –†–∞—Å—á–µ—Ç –∫–æ–Ω–≤–µ—Ä—Ç–∞—Ü–∏–∏
    result = amount * (tgt_rate / src_rate)
    
    return result

